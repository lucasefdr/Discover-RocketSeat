// comparission operators
let one = 1;
let two = 2;

// == -> igual a
console.log("==");
console.log(one == 1);
console.log(one == "1");
console.log(one == 2);

// === -> estritramente igual a
console.log("===");
console.log(one === "1");

// != -> diferente de
console.log("!=");
console.log(one != 1);
console.log(one != "1");
console.log(one != 2);

// === -> estritramente diferente de
console.log("!==");
console.log(one !== "1");

// >= -> maior ou igual a
console.log(">=");
console.log(one >= 1);
console.log(one >= 2);

// <= -> menor ou igual a
console.log("<=");
console.log(one <= 1);
console.log(one <= 2);
