// operadores de atribuição (assignment)
let x;

// assignment
x = 1;

// addition assignment -> x = x + 2
x += 2;
console.log(x);

// subtraction assignment -> x = x - 2
x -= 2;
console.log(x);

// multiplication assignment -> x = x * 2
x *= 2;
console.log(x);

// division assignment -> x = x + 2
x /= 2;
console.log(x);
